// button_defs.h
// Defines button indices for the SNES controller

#ifndef BUTTON_DEFS_H
#define BUTTON_DEFS_H

// SNES controller button definitions
#define SNES_B      0x01
#define SNES_Y      0x02
#define SNES_SELECT 0x04
#define SNES_START  0x08
#define SNES_UP     0x10
#define SNES_DOWN   0x20
#define SNES_LEFT   0x40
#define SNES_RIGHT  0x80
#define SNES_A      0x100
#define SNES_X      0x200
#define SNES_L      0x400
#define SNES_R      0x800

// Button indices for arrays
#define BTN_B      0
#define BTN_Y      1
#define BTN_SELECT 2
#define BTN_START  3
#define BTN_UP     4
#define BTN_DOWN   5
#define BTN_LEFT   6
#define BTN_RIGHT  7
#define BTN_A      8
#define BTN_X      9
#define BTN_L      10
#define BTN_R      11

#endif // BUTTON_DEFS_H
